package kr.or.study.project.dto;

import java.util.Date;

import com.fasterxml.jackson.annotation.JsonFormat;

public class ProductPrice {

	@JsonFormat(pattern = "yyyy-MM-dd'T'HH:mm:ss", timezone = "Asia/Seoul")
	private Date createDate;
	@JsonFormat(pattern = "yyyy-MM-dd'T'HH:mm:ss", timezone = "Asia/Seoul")
	private Date modifyDate;

	private int productId;
	private int productPriceId;
	private int price;
	private int discountRate;
	private String priceTypeName;

	public Date getCreateDate() {
		return createDate;
	}

	public void setCreateDate(Date createDate) {
		this.createDate = createDate;
	}

	public Date getModifyDate() {
		return modifyDate;
	}

	public void setModifyDate(Date modifyDate) {
		this.modifyDate = modifyDate;
	}

	public int getProductId() {
		return productId;
	}

	public void setProductId(int productId) {
		this.productId = productId;
	}

	public int getProductPriceId() {
		return productPriceId;
	}

	public void setProductPriceId(int productPriceId) {
		this.productPriceId = productPriceId;
	}

	public int getPrice() {
		return price;
	}

	public void setPrice(int price) {
		this.price = price;
	}

	public int getDiscountRate() {
		return discountRate;
	}

	public void setDiscountRate(int discountRate) {
		this.discountRate = discountRate;
	}

	public String getPriceTypeName() {
		return priceTypeName;
	}

	public void setPriceTypeName(String priceTypeName) {
		this.priceTypeName = priceTypeName;
	}

	@Override
	public String toString() {
		return "ProductPrice [createDate=" + createDate + ", modifyDate=" + modifyDate + ", productId=" + productId
				+ ", productPriceId=" + productPriceId + ", price=" + price + ", discountRate=" + discountRate
				+ ", priveTypeName=" + priceTypeName + "]";
	}

}
